def build_step_message(command: str, step: int, max_steps: int, camera_angle: float, memo: str = "") -> str:
    msg = (
        f"[사용자 명령]: {command}\n"
        f"카메라 각도: {camera_angle:.0f}도\n"
        f"스텝 {step}/{max_steps}."
    )
    if memo:
        msg += f"\n\n[내 메모]:\n{memo}"
    msg += "\n다음 행동을 결정하세요."
    return msg
